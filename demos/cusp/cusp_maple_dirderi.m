function y=dirderi(i,x,v,p)
switch i
case 1
t1 = exp(-(4 * x(1)));
t2 = 0.1e1 + t1;
t2 = 0.1e1 / t2 ^ 2;
y = [0.4e1 * p(1) * t2 * v(1,2) * t1 - p(2) * v(2,2) - v(1,1) 0.4e1 * p(3) * t2 * v(1,2) * t1 - v(2,1)];
case 2
t1 = exp(-(4 * x(1)));
t2 = 0.1e1 + t1;
t2 = 0.1e1 / t2;
t3 = 0.32e2 * t2 * t1 - 0.16e2;
t2 = t2 ^ 2;
t4 = v(1,2) ^ 2;
y = [p(1) * t2 * t4 * t1 * t3 p(3) * t2 * t4 * t1 * t3];
case 3
t1 = exp(-(4 * x(1)));
t2 = 0.1e1 + t1;
t2 = 0.1e1 / t2;
t3 = t1 * t2 - 0.1e1;
t4 = t2 ^ 2;
t2 = t2 * t4;
t5 = v(1,2) ^ 3;
t6 = t1 ^ 2;
y = [0.384e3 * p(1) * t2 * t5 * t6 * t3 + 0.64e2 * p(1) * t4 * t5 * t1 0.384e3 * p(3) * t2 * t5 * t6 * t3 + 0.64e2 * p(3) * t4 * t5 * t1];
case 4
t1 = exp(-(4 * x(1)));
t2 = 0.1e1 + t1;
t2 = 0.1e1 / t2;
t3 = t2 * t1;
t3 = t3 * (t3 * (0.6144e4 * t2 * t1 - 0.9216e4) + 0.3584e4) - 0.256e3;
t2 = t2 ^ 2;
t4 = v(1,2) ^ 2;
t4 = t4 ^ 2;
y = [p(1) * t2 * t4 * t1 * t3 p(3) * t2 * t4 * t1 * t3];
case 5
t1 = exp(-(4 * x(1)));
t2 = 0.1e1 + t1;
t2 = 0.1e1 / t2;
t3 = t2 * t1;
t3 = t3 * (t3 * (t3 * (0.122880e6 * t2 * t1 - 0.245760e6) + 0.153600e6) - 0.30720e5) + 0.1024e4;
t4 = v(1,2) ^ 2;
t4 = v(1,2) * t4 ^ 2;
t2 = t2 ^ 2;
y = [p(1) * t2 * t4 * t1 * t3 p(3) * t2 * t4 * t1 * t3];
end
end
